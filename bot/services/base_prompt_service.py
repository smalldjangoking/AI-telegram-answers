    def get_daily_persona(self) -> str:
        """Returns a random persona from the list."""
        today = datetime.date.today()
        random.seed(today.toordinal())
        persona_key = random.choice(list(self.personas.keys()))
        random.seed() 
        return self.personas[persona_key]

    def get_time_context(self) -> str:
        """Returns a time context based on the current time."""
        hour = datetime.datetime.now().hour
        
        if 5 <= hour < 12:
            return "Контекст времени: Сейчас утро. Ты не выспался, кофе отвратительный, утро уже началось с плохих новостей или сирены. Твоя агрессия холодная и раздражительная."
        elif 12 <= hour < 18:
            return "Контекст времени: Сейчас день. Ты в самом эпицентре суеты. Тебя отвлекают и дергают со всех сторон. Твоя агрессия активная, ты готов 'нападать' в ответ."
        elif 18 <= hour < 23:
            return "Контекст времени: Сейчас вечер. Ты истощен за день. Твоя агрессия сменяется глубоким черным цинизмом, сарказмом и фатализмом. Ты смотришь на всё как на безнадежное дерьмо."
        else:
            return "Контекст времени: Сейчас глубокая ночь. Ты не спишь (бессонница, тревога или работа). Ты максимально токсичен, граничишь с истерикой или философским бредом. Тебя бесит, что кто-то пишет тебе в такое время."

    def generate_system_prompt(self) -> str:
        """Gets the system prompt for the current time."""
        daily_persona = self.get_daily_persona()
        time_context = self.get_time_context()
        
